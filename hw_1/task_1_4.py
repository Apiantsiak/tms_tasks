# Two real numbers are given. Find the arithmetic mean and the geometric mean of these numbers.


numb_1 = int(input("Enter numb 1: "))
numb_2 = int(input("Enter numb 2: "))


average_of_numbs = (numb_1 + numb_2) / 2
geometric_mean_of_numbs = (numb_1 * numb_2) ** 0.5

print(average_of_numbs)
print(geometric_mean_of_numbs)
